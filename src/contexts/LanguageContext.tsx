// Helper function to create fallback translations
function createFallbackTranslations(sourceLang: keyof typeof translations) {
  const enKeys = Object.keys(translations[sourceLang]);
  return Object.fromEntries(
    enKeys.map(key => [key, translations[sourceLang][key]])
  ) as typeof translations.en;
}

// Add Turkish translations with English fallback
(translations as any).tr = createFallbackTranslations('en');

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState<Language>(languages[0]);
  const [selectedCurrencyCode, setSelectedCurrencyCode] = useState<string | null>(null);

  // Load saved language from localStorage on mount
  useEffect(() => {
    const savedLanguage = localStorage.getItem('selectedLanguage');
    const savedCurrency = localStorage.getItem('selectedCurrencyCode');
    if (savedLanguage) {
      const language = languages.find(lang => lang.code === savedLanguage);
      if (language) {
        setCurrentLanguage(language);
      }
    }
    if (savedCurrency) {
      setSelectedCurrencyCode(savedCurrency);
    }
  }, []);

  // Save language to localStorage when it changes and auto-set currency
  const handleSetCurrentLanguage = (language: Language) => {
    setCurrentLanguage(language);
    localStorage.setItem('selectedLanguage', language.code);
    
    // Auto-set currency based on language
    const defaultCurrency = getCurrencyForLang(language.code);
    setSelectedCurrencyCode(defaultCurrency.code);
    localStorage.setItem('selectedCurrencyCode', defaultCurrency.code);
  };

  const t = (key: string): string => {
    const currentTranslations = translations[currentLanguage.code];
    return currentTranslations[key] || key;
  };

  const getCurrencyForLang = (langCode: string) => {
    // Map UI language to a display currency; keep English and Arabic as AED by default
    switch (langCode) {
      case 'en':
        return { code: 'AED', symbol: '' };
      case 'ar':
        return { code: 'AED', symbol: '' };
      case 'fr':
      case 'de':
      case 'es':
      case 'it':
        return { code: 'EUR', symbol: '€' };
      case 'pt':
        return { code: 'BRL', symbol: 'R$' };
      case 'ru':
        return { code: 'RUB', symbol: '₽' };
      case 'zh':
        return { code: 'CNY', symbol: '¥' };
      case 'tr':
        return { code: 'TRY', symbol: '₺' };
      case 'hi':
        return { code: 'INR', symbol: '₹' };
      default:
        return { code: 'AED', symbol: '' };
    }
  };
  const defaultCurrency = getCurrencyForLang(currentLanguage.code);

  const currencyOptions: { code: string; symbol: string; label: string }[] = [
    { code: 'AED', symbol: '', label: 'AED' },
    { code: 'USD', symbol: '$', label: 'USD' },
    { code: 'EUR', symbol: '€', label: 'EUR' },
    { code: 'BRL', symbol: 'R$', label: 'BRL' },
    { code: 'RUB', symbol: '₽', label: 'RUB' },
    { code: 'CNY', symbol: '¥', label: 'CNY' },
    { code: 'TRY', symbol: '₺', label: 'TRY' },
    { code: 'INR', symbol: '₹', label: 'INR' },
  ];

  const getCurrencyMeta = (code: string) => {
    const found = currencyOptions.find(c => c.code === code);
    if (found) return found;
    return currencyOptions[0];
  };

  const effectiveCurrencyCode = selectedCurrencyCode || defaultCurrency.code;
  const effectiveCurrency = getCurrencyMeta(effectiveCurrencyCode);
  const currencyCode = effectiveCurrency.code;
  const currencySymbol = effectiveCurrency.symbol;
  const currencyIconSrc = currencyCode === 'AED' ? '/dhiram.png' : null;

  // Simple static conversion rates from AED → target currency (approx; replace with API if needed)
  const getRateForCurrency = (code: string): number => {
    switch (code) {
      case 'AED':
        return 1.0;
      case 'USD':
        return 0.27; // approx
      case 'EUR':
        return 0.25; // approx
      case 'BRL':
        return 1.48; // approx
      case 'RUB':
        return 26; // approx
      case 'CNY':
        return 1.95; // approx
      case 'TRY':
        return 8.5; // approx
      case 'INR':
        return 22.5; // approx
      default:
        return 1.0;
    }
  };
  const conversionRate = getRateForCurrency(currencyCode);

  const convertFromAED = (amountAED: number): number => {
    if (!amountAED || Number.isNaN(amountAED)) return 0;
    return Math.round(amountAED * conversionRate);
  };

  const formatPrice = (amountAED?: number | null): string => {
    if (!amountAED && amountAED !== 0) return '';
    const converted = convertFromAED(amountAED as number);
    return `${currencySymbol ? currencySymbol + ' ' : ''}${converted.toLocaleString()}`;
  };

  const setCurrency = (code: string | null) => {
    setSelectedCurrencyCode(code);
    if (code) {
      localStorage.setItem('selectedCurrencyCode', code);
    } else {
      localStorage.removeItem('selectedCurrencyCode');
    }
  };

  return (
    <LanguageContext.Provider
      value={{
        currentLanguage,
        setCurrentLanguage: handleSetCurrentLanguage,
        t,
        languages,
        currencyCode,
        currencySymbol,
        currencyIconSrc,
        availableCurrencies: currencyOptions,
        setCurrency,
        convertFromAED,
        formatPrice,
      }}
    >
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
